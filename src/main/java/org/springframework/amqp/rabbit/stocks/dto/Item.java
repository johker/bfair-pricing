package org.springframework.amqp.rabbit.stocks.dto;

public class Item {
	
	private double size; 
	
	private double price;

	
	public double getSize() {
		return size;
	}

	public void setSize(double size) {
		this.size = size;
	}


	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	} 
	
	
	

}
